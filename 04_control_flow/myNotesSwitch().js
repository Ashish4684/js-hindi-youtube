// ********any data type can be compared in switc case (just put the value in cases)***************

let month = 2
switch (month) {
    case 1:
        console.log("january");
        break;
    case 2:
        console.log("feb");
        break;
    case 3:
        console.log("march");
        break;

    default: console.log("year end");
        break;
}
